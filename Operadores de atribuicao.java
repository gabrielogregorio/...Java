package com.operadores_de_atribuicai;

public class operadores_de_atribuicao {
	public static void main(String[] args) {
		
		int x = 1;
		int y = 10;
		System.out.println( x += y); // 11
		System.out.println( x -= y); // 1 
		System.out.println( x *= y); // 10 
		System.out.println( x /= y); // 1 
		System.out.println( x %= y); // 1 
	}
}
